#Encapsulamiento

#El encapsulamiento o encapsulación en programación es un concepto relacionado con la programación orientada 
# a objetos, y hace referencia al ocultamiento de los estado internos de una clase al exterior. Dicho de otra manera,
#  encapsular consiste en hacer que los atributos o métodos internos a una clase no se puedan acceder ni modificar desde 
# fuera, sino que tan solo el propio objeto pueda acceder a ellos.